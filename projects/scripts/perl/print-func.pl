# About print function.
$s = "hello";
$n = 123;

#print takes multiple parameters, print without space.
print $s, $n, "\n";
#string is zero when adding to number!
print $s + $n , "\n"; 
#string can be concatenated by dot
print $s . $n . "\n";

# formal parenthesis
print('$s World.\n');
# double quotes.
print("$s World.\n");
